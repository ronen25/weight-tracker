import { useCallback, useEffect } from "react";
import { useAtom } from "jotai";
import type { GetServerSideProps } from "next";
import Head from "next/head";
import UserDetailsAtom from "../atoms/UserDetailsAtom";
import type UserData from "../models/UserData";
import { getServerAuthSession } from "../server/common/get-server-auth-session";
import { prisma } from "../server/db/client";
import { useQuery } from "@tanstack/react-query";
import LoadingSpinner from "../components/LoadingSpinner";
import WeightItem from "../components/WeightItem";

interface Props {
  userData: UserData;
}

const Home = ({ userData }: Props) => {
  const [_, setUserData] = useAtom(UserDetailsAtom);
  const { isLoading, isError, data, error } = useQuery(["weights"], () =>
    fetch("/api/weights").then((res) => res.json())
  );

  useEffect(() => {
    setUserData(userData);
  }, []);

  const showDataScreen = useCallback(() => {
    if (isError) {
      return <div>ERROR: {JSON.stringify(error)}</div>;
    }

    // TODO: Remove index used here
    return (
      <ul className="sm:w-4/5">
        {new Array(1000).fill(data[0]).map((item, index) => (
          <WeightItem key={index} weight={item} />
        ))}
      </ul>
    );
  }, [isError, data, error]);

  const greeting = () => {
    const currentDate = new Date();
    const hour = currentDate.getHours();

    let format = "";
    if (hour >= 6 && hour < 11) {
      format = "Good morning";
    } else if (hour >= 11 && hour < 16) {
      format = "Good day";
    } else if (hour >= 16 && hour <= 18) {
      format = "Good evening";
    } else {
      format = "Good night";
    }

    return `${format}, ${userData.firstName}`;
  };

  return (
    <>
      <Head>
        <title>Weight Tracker</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="flex h-full flex-col">
        <h1 className="bg-slate-50 py-2 text-center text-3xl font-bold">
          {greeting()}
        </h1>

        <div className="flex grow items-center justify-center">
          {isLoading ? <LoadingSpinner /> : showDataScreen()}
        </div>
      </main>
    </>
  );
};

export const getServerSideProps: GetServerSideProps = async ({ req, res }) => {
  const session = await getServerAuthSession({ req, res });

  if (!session?.user) {
    return {
      redirect: {
        destination: "/login",
        permanent: false,
      },
    };
  }

  const userData = await prisma.users.findFirst({
    where: {
      id: session.user?.id,
    },
  });

  return {
    props: {
      userData: {
        id: userData?.id,
        firstName: userData?.first_name,
        lastName: userData?.last_name,
        email: userData?.email,
        sex: userData?.sex,
        dob: userData?.dob?.toUTCString(),
        avatarUrl: userData?.avatar_url,
      },
    },
  };
};

export default Home;
